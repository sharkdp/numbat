"""
This script:
1. Generates example files from examples/*.nbt
2. Builds the `inspect` program once
3. Generates function reference files via `inspect`
4. Generates the units list via `inspect`
5. Runs zensical build
"""

from concurrent.futures import ThreadPoolExecutor
import json
import subprocess
from pathlib import Path
import urllib.parse
import os
import sys


SCRIPT_DIR = Path(__file__).parent.resolve()
WORKSPACE_DIR = SCRIPT_DIR.parent
NUMBAT_CRATE_DIR = WORKSPACE_DIR / "numbat"
MODULES_DIR = NUMBAT_CRATE_DIR / "modules"


def inspect_env():
    env = os.environ.copy()
    env["TZ"] = "UTC"
    return env


def inspect_binary_path():
    metadata = subprocess.run(
        ["cargo", "metadata", "--format-version=1", "--no-deps"],
        capture_output=True,
        text=True,
        check=True,
        cwd=WORKSPACE_DIR,
    )
    target_directory = Path(json.loads(metadata.stdout)["target_directory"])
    binary_name = "inspect.exe" if os.name == "nt" else "inspect"
    return target_directory / "debug" / "examples" / binary_name


def build_inspect_binary():
    print("Building inspect example (debug)...")
    command = [
        "cargo",
        "build",
        "--quiet",
        "--example",
        "inspect",
        "-p",
        "numbat",
    ]

    subprocess.run(
        command,
        check=True,
        cwd=WORKSPACE_DIR,
    )

    binary = inspect_binary_path()
    if not binary.exists():
        print(f"Could not find inspect binary at {binary}", file=sys.stderr)
        sys.exit(1)
    return binary


def run_inspect(inspect_binary, *args):
    result = subprocess.run(
        [str(inspect_binary), str(MODULES_DIR), *args],
        capture_output=True,
        text=True,
        env=inspect_env(),
        check=True,
        cwd=WORKSPACE_DIR,
    )
    return result.stdout


def generate_example(
    filename, title, strip_asserts=True, insert_run_link=True, footer=None, icon=None
):
    """Generate a markdown file from a Numbat example file."""
    path_in = SCRIPT_DIR.parent / "examples" / f"{filename}.nbt"
    path_out = SCRIPT_DIR / "src" / "examples" / f"example-{filename}.md"
    print(f"Generating: {path_out}")

    code = []
    with open(path_in, "r", encoding="utf-8") as fin:
        for line in fin:
            if not (strip_asserts and "assert_eq" in line):
                code.append(line)

    url = f"https://numbat.dev/?q={urllib.parse.quote_plus(''.join(code))}"

    with open(path_out, "w", encoding="utf-8") as fout:
        if icon:
            fout.write("---\n")
            fout.write(f"icon: {icon}\n")
            fout.write("---\n")
            fout.write("\n")
        fout.write("<!-- This file is autogenerated! Do not modify it -->\n")
        fout.write("\n")
        fout.write(f"# {title}\n")
        if insert_run_link:
            fout.write("\n")
            fout.write(
                f"[:material-play-circle: Run this example]({url}){{ .md-button .md-button--primary }}\n"
            )
        fout.write("\n")
        fout.write("```numbat\n")
        fout.writelines(code)
        fout.write("```\n")

        if footer:
            fout.write("\n")
            fout.write(footer)
            fout.write("\n")


def xkcd_footer(number, img_name):
    """Generate footer HTML for XKCD comics."""
    footer = '<p align="center" style="margin-top: 2em">'
    footer += f'<a href="https://xkcd.com/{number}/"><img src="https://imgs.xkcd.com/comics/{img_name}.png" alt="XKCD {number}" style="max-width: 100%"></a>'
    footer += f'<br>Source: <a href="https://xkcd.com/{number}/">https://xkcd.com/{number}/</a>'
    footer += "</p>"
    return footer


